package domain.statements.dom.impl;

import javax.jdo.annotations.IdGeneratorStrategy;
import javax.jdo.annotations.IdentityType;
import javax.jdo.annotations.VersionStrategy;

import org.apache.isis.applib.annotation.Auditing;
import org.apache.isis.applib.annotation.DomainObject;
import org.apache.isis.applib.annotation.DomainObjectLayout;

import domain.statements.dom.AbstractNamedEntity;
import domain.statements.dom.types.Notes;

@javax.jdo.annotations.PersistenceCapable(identityType=IdentityType.DATASTORE, schema = "simple")
@javax.jdo.annotations.DatastoreIdentity(strategy=IdGeneratorStrategy.IDENTITY, column="id")
@javax.jdo.annotations.Version(strategy= VersionStrategy.DATE_TIME, column="version")
@javax.jdo.annotations.Unique(name="SimpleObject_name_UNQ", members = {"name"})
@DomainObject(auditing = Auditing.ENABLED)
@DomainObjectLayout()  // causes UI events to be triggered
public class SimpleObject extends AbstractNamedEntity<SimpleObject> {

    public SimpleObject(String name) {
		super(name);
	}

	@lombok.Getter @lombok.Setter
    @Notes private String notes;

	@Override
	protected String getTitlePrefix() {
		return "OBJ "; //$NON-NLS-1$
	}

}